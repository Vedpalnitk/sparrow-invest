async function seedProviders() {
  console.log('üè¢ Seeding providers (AMCs)...');

  for (const provider of PROVIDERS) {
    await prisma.provider.upsert({
      where: { id: provider.id },
      update: {
        name: provider.name,
        shortName: provider.shortName,
        sebiRegNo: provider.sebiRegNo,
        isActive: true,
      },
      create: {
        id: provider.id,
        name: provider.name,
        shortName: provider.shortName,
        sebiRegNo: provider.sebiRegNo,
        isActive: true,
      },
    });
  }

  console.log(`  ‚úì ${PROVIDERS.length} providers seeded`);
}

async function seedSchemes() {
  console.log('üìä Seeding schemes...');

  for (const scheme of SCHEMES) {
    // Create/update scheme
    await prisma.scheme.upsert({
      where: { id: scheme.id },
      update: {
        name: scheme.name,
        categoryId: scheme.categoryId,
        subCategory: scheme.subCategory,
        benchmark: scheme.benchmark,
        fundManager: scheme.fundManager,
        investmentObjective: scheme.investmentObjective,
        exitLoad: scheme.exitLoad,
        lockinPeriod: schemesWithLockin[scheme.id] || null,
        mfapiSchemeCode: scheme.mfapiSchemeCode,
        status: 'active',
      },
      create: {
        id: scheme.id,
        providerId: scheme.providerId,
        name: scheme.name,
        categoryId: scheme.categoryId,
        subCategory: scheme.subCategory,
        benchmark: scheme.benchmark,
        fundManager: scheme.fundManager,
        investmentObjective: scheme.investmentObjective,
        exitLoad: scheme.exitLoad,
        lockinPeriod: schemesWithLockin[scheme.id] || null,
        mfapiSchemeCode: scheme.mfapiSchemeCode,
        status: 'active',
      },
    });

    // Create/update scheme plans
    for (const plan of scheme.plans) {
      await prisma.schemePlan.upsert({
        where: { id: plan.id },
        update: {
          isin: plan.isin,
          rtaIdentifier: plan.rtaIdentifier,
          amfiIdentifier: plan.amfiIdentifier,
          name: plan.name,
          plan: plan.plan,
          option: plan.option,
          mfapiSchemeCode: plan.mfapiSchemeCode,
          purchaseAllowed: true,
          redemptionAllowed: true,
          sipAllowed: true,
          switchInAllowed: true,
          switchOutAllowed: true,
          status: 'active',
        },
        create: {
          id: plan.id,
          schemeId: scheme.id,
          isin: plan.isin,
          rtaIdentifier: plan.rtaIdentifier,
          amfiIdentifier: plan.amfiIdentifier,
          name: plan.name,
          plan: plan.plan,
          option: plan.option,
          mfapiSchemeCode: plan.mfapiSchemeCode,
          purchaseAllowed: true,
          redemptionAllowed: true,
          sipAllowed: true,
          switchInAllowed: true,
          switchOutAllowed: true,
          status: 'active',
        },
      });

      // Create/update NAV
      const today = new Date();
      await prisma.schemePlanNav.upsert({
        where: { schemePlanId: plan.id },
        update: {
          navDate: today,
          nav: plan.nav,
          dayChange: plan.dayChange,
          dayChangePct: (plan.dayChange / (plan.nav - plan.dayChange)) * 100,
        },
        create: {
          schemePlanId: plan.id,
          navDate: today,
          nav: plan.nav,
          dayChange: plan.dayChange,
          dayChangePct: (plan.dayChange / (plan.nav - plan.dayChange)) * 100,
        },
      });

      // Create/update metrics
      await prisma.schemePlanMetrics.upsert({
        where: { schemePlanId: plan.id },
        update: {
          return1m: plan.return1m,
          return3m: plan.return3m,
          return6m: plan.return6m,
          return1y: plan.return1y,
          return3y: plan.return3y,
          return5y: plan.return5y,
          riskRating: plan.riskRating,
          fundRating: plan.fundRating,
          expenseRatio: plan.expenseRatio,
          aum: plan.aum,
          aumDate: today,
          volatility: plan.volatility,
          sharpeRatio: plan.sharpeRatio,
        },
        create: {
          schemePlanId: plan.id,
          return1m: plan.return1m,
          return3m: plan.return3m,
          return6m: plan.return6m,
          return1y: plan.return1y,
          return3y: plan.return3y,
          return5y: plan.return5y,
          riskRating: plan.riskRating,
          fundRating: plan.fundRating,
          expenseRatio: plan.expenseRatio,
          aum: plan.aum,
          aumDate: today,
          volatility: plan.volatility,
          sharpeRatio: plan.sharpeRatio,
        },
      });
    }
  }

  const totalPlans = SCHEMES.reduce((sum, s) => sum + s.plans.length, 0);
  console.log(`  ‚úì ${SCHEMES.length} schemes with ${totalPlans} plans seeded`);
}

async function main() {
  console.log('üå± Seeding fund data...\n');

  await seedProviders();
  await seedSchemes();

  // Print summary
  const providerCount = await prisma.provider.count();
  const schemeCount = await prisma.scheme.count();
  const planCount = await prisma.schemePlan.count();
  const navCount = await prisma.schemePlanNav.count();
  const metricsCount = await prisma.schemePlanMetrics.count();

  console.log('\n‚úÖ Fund seeding completed!');
  console.log(`   Providers: ${providerCount}`);
  console.log(`   Schemes: ${schemeCount}`);
  console.log(`   Plans: ${planCount}`);
  console.log(`   NAV records: ${navCount}`);
  console.log(`   Metrics records: ${metricsCount}`);
}

main()
  .catch((e) => {
    console.error('‚ùå Fund seeding failed:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
